import time


def solution(map):
    w, h = len(map[0]), len(map)
    found = []
    dfs((0, 0), (h - 1, w - 1), [], False, found, map)
    return len(min(found, key=len))


def get_neighbours(point, end):
    get = []
    neighbours = [
        (-1, +0), (+0, -1), (+0, +0), (+0, +1), (+1, +0)
    ]
    for n in neighbours:
        y, x = [point[i] + n[i] for i in [0, 1]]
        if end[0] >= y >= 0 and end[1] >= x >= 0:
            get.append((y, x))
    return get


def dfs(point, end, visited, removed, found, map):
    visited.append(point)
    if map[point[0]][point[1]] == 1:
        if removed:
            return
        else:
            removed = True
    if point == end:
        return found.append(visited)
    neighbours = get_neighbours(point, end)
    for n in neighbours:
        if n not in visited:
            dfs(n, end, visited[:], removed, found, map)


